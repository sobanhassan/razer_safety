                        </ul>
                        <p>On Nov. 2, 2015, President Obama signed the Bipartisan Budget Act of 2015 (Act), which is a
                            two-year budget deal. One provision in the Act will allow <a href="http://www.osha.gov/"
                                target="_blank">OSHA</a> to increase the maximum penalty amounts it imposes on employers
                            that violate occupational safety and health standards. These increases will become
                            effective <span data-term="goog_1723732909" tabindex="0">Aug. 1, 2016</span>.</p>
                        <h2>Justification for Increased OSHA Penalties</h2>
                        <p>Many government agencies have the authority to adjust their maximum penalty amounts annually
                            to reflect the cost of inflation, as shown by the consumer price index (CPI). However, OSHA
                            has not been allowed to adjust its maximum penalty amounts since the penalties were enacted
                            25 years ago. The Act will allow OSHA to adjust the maximum penalty amounts to reflect
                            inflation.</p>
                        <blockquote>
                            <p>OSHA will be allowed to increase its maximum penalties in 2016 and adjust them annually
                                for inflation starting in 2017</p>
                        </blockquote>
                        <h3>Catch-up Increase</h3>
                        <p>The Act requires OSHA to implement the new maximum penalty amounts in two phases:</p>
                        <ul>
                            <li>An initial catch-up adjustment.</li>
                            <li>An ongoing subsequent adjustment period.</li>
                        </ul>
                        <p>The initial catch-up period took place in 2016 and  allowed OSHA to make a cost-of-living
                            adjustment to account for the previous 25 years of non-adjustment. This catch-up increase is
                            capped at 150 percent of current OSHA penalty levels.<br />
                            The Act requires OSHA to indicate the actual amount of the catch-up increase through an
                            interim final rule. Most predictions and speculations surrounding the actual increase
                            amounts hover around an 80 percent increase, because the current CPI index is around 78.2
                            percent higher than what it was when OSHA’s current penalties were adopted.<br />
                            However, the Act also gives OSHA the discretion to increase the current maximum penalty
                            amounts by less than the cost-of-living adjustment if it determines that the full increase
                            will:</p>
                        <ul type="disc">
                            <li>Have a negative effect on the economy.</li>
                            <li>Result in social costs that outweigh the benefits of the full increase. The Office of
                                Management and Budget must agree with this determination.</li>
                        </ul>
                        The Act requires OSHA to issue a proposed rule and solicit public comments if it plans to
                        increase the penalty amounts by less than the cost-of-living adjustment. The Act requires OSHA
                        to publish by <strong>July 1, 2016</strong>, how it will implement the first inflation
                        adjustment. The first adjustment must become effective on&nbsp;<strong>Aug. 1, 2016</strong>.
