                        <h2>Annual Adjustments</h2>
                        <p>After the initial catch-up provision takes place in 2016, the Act allows OSHA to update its
                            maximum penalty amounts for inflation each year.</p>
                        <p>The first annual inflation adjustment will be allowed for 2017. OSHA will be required to
                            publish annual updates by <span data-term="goog_1723732911" tabindex="0">Jan. 15,
                                2017</span>, to signal this increase and every year thereafter in the Federal Register.
                        </p>
