                        <h2>Quick OSHA Penalties Facts:</h2>
                        <ul>
                            <li>The Bipartisan Budget Act (Act) was signed into law on Nov. 2, 2015</li>
                            <li>The Act allows OSHA to increase the maximum penalties for safety and health violations
