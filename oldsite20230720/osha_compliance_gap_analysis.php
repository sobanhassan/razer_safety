                <p>Written  safety and health plans are the heartbeat of a company’s safety and health  program. Not only does&nbsp;OSHA&nbsp;require companies to have written programs,  but also contractors often require safety and health programs to be submitted  as part of the pre-qualification process for bidding jobs. Insurance brokers  and underwriters are eager to see written safety programs and how the programs  keep employees safe and healthy at the workplace.<br />
                  Throughout  the review process, Razer Safety &amp; Health, LLC, will discuss your company’s  operation in detail to ensure the program will meet your specific needs and will  address hazards that your employees may encounter. Razer Safety will provide, at  your request, a written compliance gap analysis. In addition, information will  be obtained for implementing your safety and health program, hazards specific  to your operation, personal protective equipment that is utilized, evacuation  routes and procedures, and more. Once your company’s information has been  obtained and reviewed, Razer Safety will be able to create a customized safety  program for your company that addresses safety conditions unique to your  company. You can depend on Razer Safety to develop programs to best meet your  company’s requirements. We will update your programs as standards change.</p>
