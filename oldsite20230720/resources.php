                    <h2>How to Avoid OSHA's &quot;Fatal Four&quot; Construction Hazards</h2>
                    <p>In the  construction industry, certain hazards are present every day on the job site.<br />
The  Occupational Safety &amp; Health Administration (OSHA) has identified the four  leading causes of fatalities in the construction industry – known as OSHA&rsquo;s  &ldquo;Fatal Four Hazards&rdquo; or &quot;Construction Focus Four Hazards&quot;.<br />
The <a href="https://www.osha.gov/oshstats/commonstats.html" target="_blank" title="Opens in a new window">Fatal Four Hazards</a> consist of falls, electrical exposure, struck-by and caught-in/between  situations, and result in 545 worker fatalities in the United States every  year.<br />
<strong>Fall  Hazards </strong><em>- </em>Harness Inspection Guidelines https://www.osha.gov/sites/default/files/2018-12/fy15_sh-27664-sh5_Lifeline_Harness_Inspection_Guide.pdf <br />
Falls  are the number one killer of construction workers on the job.<br />
The  different heights at which fall protection is required are:</p>
                    <ul type="disc">
                      <li>6 feet - General fall protection</li>
                      <li>10 feet - Scaffolds</li>
                      <li>15 to 30 feet - Steel erection</li>
                    </ul>
                    <p>If  a worker is performing a job above one of the heights mentioned above, one of  these systems must be in place:</p>
                    <ul type="disc">
                      <li>Guardrail Systems</li>
                      <li>Covers</li>
                      <li>Personal Fall Arrest (PFA) systems</li>
                    </ul>
                    <p>Make  sure to identify fall hazards such as holes in platforms and unguarded edges  before work begins.<br />
                      <strong>Electrocutions</strong> <br />
                      One  of the top ten most cited OSHA standards; electrocutions are the second  deadliest fatal four construction hazard. That&rsquo;s why OSHA has a specific  regulatory standard for safety training and electrical work.<br />
                      Here  are some safe work practices for live electrical work on the jobsite:</p>
                    <ul type="disc">
                      <li>Locate and identify utilities before       starting work.</li>
                      <li>Look for overhead power lines when       operating any equipment.</li>
                      <li>Maintain a safe distance away from power       lines.</li>
                      <li>Do not operate portable electric tools       unless they are grounded or double insulated.</li>
                      <li>Use ground-fault surge protectors.</li>
                      <li>Be alert to electrical hazards when       working with ladders, scaffolds or other platforms.</li>
                    </ul>
                    <p><strong>Struck-By Object/Hazard</strong> <br />
                      A  struck-by hazard is anything at a worksite that could produce injuries by  forcible contact or impact between the injured person and an object or piece of  equipment.<br />
                      The  difficulty with protecting workers from flying objects is that they may not be  participating in the work that causes the object to fly.<br />
                      Workers  need to make sure coworkers follow the safety rules outlined by OSHA and the  employer, and wear appropriate personal protective equipment (PPE) for the  worksite.<br />
                      There  are four Struck-by hazard categories:</p>
                    <ul type="disc">
                      <li>Flying object</li>
                      <li>Falling object</li>
                      <li>Swinging object</li>
                      <li>Rolling object</li>
                    </ul>
                    <p>OSHA  requires employers to protect workers from struck-by hazards. Employers are  required to:</p>
                    <ul type="disc">
                      <li>Meet all heavy equipment, motor vehicle,       and general requirements included in: <a href="https://www.osha.gov/laws-regs/regulations/standardnumber/1911/1911.10">https://www.osha.gov/laws-regs/regulations/standardnumber/1911/1911.10</a> Construction Standards</li>
                      <li>Provide proper PPE, like wearing       high-visibility clothes near equipment/vehicles.</li>
                      <li>Provide required training for equipment       and vehicles</li>
                      <li>Ensure qualification of operators,       riggers, signal persons, and competent persons</li>
                    </ul>
              <p><strong>Caught-in/Between</strong> <br />
                      Trench  and excavation cave-ins, and workplace accidents where workers are pinned or  caught-between machinery or fixed structures, often happen suddenly and without  warning.<br />
                      To  avoid being caught in or between objects:</p>
                    <ul type="disc">
                      <li>Never enter an unprotected trench 5 feet       or deeper without an adequate protective system in place</li>
                      <li>Make sure the trench or excavation is protected  either by sloping, shoring, benching or trench shield systems.</li>
              </ul>
