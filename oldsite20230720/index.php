                        <h2>OSHA Final Rule to Improve Tracking of Workplace Injuries and Illnesses</h2>
                        <p>Many employers with more than 10 employees – including electrical contractors around the
                            country – are required by the Occupational Safety and Health Administration to keep a record
                            of serious work-related injuries and illnesses. Minor injuries requiring first aid only do
                            not need to be recorded.<br />
                            As part of a new reporting rule issued by OSHA, all employers will be required to submit
                            their 300A Summary electronically beginning on Jan. 1, 2017.<br />
                            Companies with 250 or more employees must start providing additional information in 2018,
                            including the 300A Summary, the 300 log form and all 301 or incident reports that have been
                            purged of employee and physician names and addresses.<br />
                            OSHA has given all employers of 20-249 employees until July 1, 2017, to submit the 300A
                            electronically via its web portal for the calendar year 2016. The deadline is July 1, 2018,
                            for calendar year 2017.<br />
                            OSHA does not prohibit post-incident testing and incentive programs as long as they do not
                            discourage reporting of injuries in any way. If a contractor, for instance, only did
                            post-incident testing after an incident or accident, that could be deemed discriminatory and
                            possibly discourage reporting.<br />
                            A Drug Free Work Place program that has pre-employment testing, random testing and
                            reasonable suspicion testing, along with post-incident testing, would be a program that does
                            not discriminate against an employee simply for being involved in an accident or incident.
                            The total program would discourage drug use in the workplace and protect all employees from
                            these exposures.</p>
                        <p>For additional information on OSHA’s webpage on the new reporting rule, go to <a
                                href="https://www.osha.gov/recordkeeping/finalrule/index.html" target="_new">OSHA's
                                Final Rule</a>.</p>
