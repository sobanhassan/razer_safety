		      <h2>WHAT about Silica???
              </h2>
			  <h2>Let Razer Safety and Health, LLC assist you in navigating the new OSHA Silica Standards.
              </h2>
			  <p>Occupational  Safety and Health Administration <a href="https://www.gpo.gov/fdsys/pkg/FR-2016-03-25/pdf/2016-04800.pdf" target="_blank">(OSHA) published  its final rule</a> setting new workplace permissible exposure limits (PELs) for respirable  crystalline silica.&nbsp;June 23, 2017, was the deadline for compliance for  employers in the construction industry, but a 90-day hold was instituted making  the <strong>new compliance date  September 23, 2017.</strong> In addition to considerably tightening  exposure limits, the new rule outlines methods to control silica exposure,  procedures for conducting medical exams for workers with high exposure,  training obligations for workers about silica-related hazards and  record-keeping requirements related to crystalline silica quantities. Under the  new rule, the PEL (personal exposure limit) is now 50 micrograms of respirable  crystalline silica per cubic meter of air (μg/m3), averaged over an eight-hour  day. OSHA requires action if the exposure is measured at 25 micrograms per  cubic meter of air.
			  <p>Those who do not comply with the new OHSA standard <a href="https://www.osha.gov/penalties/" target="_blank">will  be subject to</a> a maximum fine of $12,675 for a serious or other-than-serious  violation; $12,675 per day past the abatement date for a failure-to-abate  violation; and $126,749 for a repeated or willful violation.                            
			  <p> What  is Crystalline Silica?&nbsp; Find answers in the OSHA Fact Sheet at: <a href="https://www.osha.gov/OshDoc/data_General_Facts/crystalline-factsheet.pdf" target="_blank">https://www.osha.gov/OshDoc/data_General_Facts/crystalline-factsheet.pdf</a>
			  <p>OSHA  estimates that two million construction workers today are exposed to respirable  crystalline silica in more than 600,000 workplaces nationwide – about 840,000  of those workers, OSHA estimates, are currently exposed to silica levels that  exceed the new PEL of 50 μg/m3.
			  <p>OSHA  has published two fact sheets summarizing the requirements of the new  standards: a <a href="https://www.osha.gov/Publications/OSHA3681.pdf" target="_blank">Construction  Fact Sheet</a> and a <a href="https://www.osha.gov/Publications/OSHA3682.pdf" target="_blank">General Industry and Maritime Fact  Sheet</a>.
			  <p> Construction  companies can refer to the following PDF for guidance: <a href="https://www.osha.gov/silica/SilicaConstructionRegText.pdf" target="_new">https://www.osha.gov/silica/SilicaConstructionRegText.pdf.</a>
			  <p>Full-shift  sampling representing workplace exposures for silica are conducted for  determination of <a href="https://www.osha.gov/SLTC/samplinganalysis/" target="_blank">OSHA compliance,  respiratory hazard and health risk</a>.
